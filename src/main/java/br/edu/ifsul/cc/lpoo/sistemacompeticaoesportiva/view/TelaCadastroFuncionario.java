    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(funcionario == null){
            funcionario = new Funcionario();
        }
        
        funcionario.setNome(txtNome.getText());
        funcionario.setCargo(txtCargo.getText());
        funcionario.setDepartamento((Departamento) cmbDepartamentos.getSelectedItem());
        
        jpa.conexaoAberta();
        try {
            jpa.persist(funcionario);
            System.out.println("Funcionario cadastrado com sucesso");
        } catch (Exception ex) {
            Logger.getLogger(TelaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("Erro ao persistir funcion√°rio: "+funcionario+" \n Erro: "+ex);
        }
        jpa.fecharConexao();
        dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

    public void loadDepartamentos() {
        cmbDepartamentos.removeAllItems();
        cmbDepartamentos.addItem(null);
        jpa.conexaoAberta();

        for (Departamento d : jpa.getDepartamentos()) {
            cmbDepartamentos.addItem(d);
        }

        jpa.fecharConexao();
    }
    
