    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(tarefa == null){
            tarefa = new Tarefa();
        }
        
        tarefa.setDescricao(txtPlaca.getText());
        tarefa.setProjeto((Projeto) cmbProjetos.getSelectedItem());
        tarefa.setFuncionario((Funcionario) cmbFuncionarios.getSelectedItem());
        
        jpa.conexaoAberta();
        try {
            jpa.persist(tarefa);
            System.out.println("Tarefa cadastrada com sucesso");
        } catch (Exception ex) {
        }
        jpa.fecharConexao();
        dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

