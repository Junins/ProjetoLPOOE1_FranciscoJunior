    public void loadFuncionarios() {
        cmbFuncionarios.removeAllItems();
        cmbFuncionarios.addItem(null);
        jpa.conexaoAberta();

        for (Funcionario f : jpa.getFuncionarios()) {
            cmbFuncionarios.addItem(f);
        }

        jpa.fecharConexao();
    }
    
        public void loadProjetos() {
        cmbProjetos.removeAllItems();
        cmbProjetos.addItem(null);
        jpa.conexaoAberta();

        for (Projeto p : jpa.getProjetos()) {
            cmbProjetos.addItem(p);
        }

        jpa.fecharConexao();
    }

